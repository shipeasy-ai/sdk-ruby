def read(rel)
  File.read(File.join(DOCS, rel))
end

# Rewrite relative `(page.md)` links (as they appear inside docs/pages/) to
# absolute GitHub blob URLs, so pulled prose links work from the repo root.
def absolutize(text)
  text.gsub(%r{\]\((?!https?://|/)([\w./-]+?\.md)(#[\w-]+)?\)}) do
    "](#{BLOB}/docs/pages/#{Regexp.last_match(1)}#{Regexp.last_match(2)})"
  end
end

def first_code(md, lang)
  m = md.match(/```#{lang}\n(.*?)```/m)
  m ? m[1].rstrip : ""
end

def lead_paragraph_md(md)
  out = []
  started = false
  md.each_line do |ln|
    unless started
      started = ln.start_with?("# ")
      next
    end
    s = ln.strip
    if s.empty?
      break unless out.empty?

      next
    end
    if s.start_with?("#", "```", ">", "|", "-", "<")
      break unless out.empty?

      next
    end
    out << s
  end
  out.join(" ").gsub(/\s+/, " ").strip
end

def lead_paragraph(md)
  text = lead_paragraph_md(md)
  text = text.gsub(/`([^`]+)`/, '\1')
  text = text.gsub(/\*\*(.+?)\*\*/, '\1')
  text.gsub(/\[(.+?)\]\([^)]*\)/, '\1').strip
end

manifest = JSON.parse(read("manifest.json"))
pages = manifest["pages"]
sdk = manifest["sdk"]

install   = first_code(read("pages/installation.md"), "ruby")
