quickstart = first_code(read("pages/overview.md"), "ruby")
testing_md   = read("pages/testing.md")
testing_lead = absolutize(lead_paragraph_md(testing_md))
testing_code = first_code(testing_md, "ruby")

rows = pages.keys.map do |key|
  title = PAGE_TITLE.fetch(key, key.split("-").map(&:capitalize).join(" "))
  blurb = PAGE_BLURB[key] || lead_paragraph(read(pages[key]))
  blurb = "#{blurb[0, 87].rstrip}…" if blurb.length > 90
  "| [#{title}](#{BLOB}/docs/pages/#{key}.md) | #{blurb} |"
end
docs_table = rows.join("\n")

